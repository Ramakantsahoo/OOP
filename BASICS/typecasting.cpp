#include <bits/stdc++.h>
using namespace std;
int main()
{
    long double a = 19.548;
    int b = 10;
    long double sum = a + b;
    cout << sum << endl;
    cout << int(sum) << endl;
    return 0;
}
// Terminal
// 29.548
// 29
